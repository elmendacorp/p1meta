public class CosteFrecuencia {
    private int frecuencia;
    private int coste;

    public CosteFrecuencia(int fr, int punt) {
        frecuencia = fr;
        coste = punt;
    }

    public int getFrecuencia() {
        return frecuencia;
    }

    public int getCoste() {
        return coste;
    }
}
